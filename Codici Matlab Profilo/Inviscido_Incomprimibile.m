%Calcolo del centro di pressione per gli angoli in questione. Il Cm
%calcolato poc'anzi risulta rispetto al punto al 25% della corda, non
%necessariamente corrispondente con il centro aerodinamico. Dunque,
%bisogner calcolare il centro aerodinamico e graficare il Cm rispetto a
%tale punto, verificando l'andamento costante della retta.

Cmle = -0.25*Cl + Cmc4;                                                 %calcolo Cm rispetto al leading edge

xcp = - Cmle./Cl;

xcp(length(xcp) + 1) = 0;                                               %codice per aumentare la lunghezza del vettore e metterci un valore positivo
                                                                        %random (40) in mezzo, rende il grafico pi leggibile
for i = 152:length(xcp)
   xcp(length(xcp) + 152 - i) = xcp(length(xcp) + 151 - i);
end

xcp(152) = 40;

alfacopy = alfa;

alfa(length(alfa) + 1) = 0;

for i = 152:length(alfa)
   alfa(length(alfa) + 152 - i) = alfa(length(alfa) + 151 - i);
end
alfa(152) = -4.95;

figure;                                                                 %plot centro di pressione
plot(alfa(1:151),xcp(1:151),'k','LineWidth',1); hold on; grid on;
plot(alfa(152:end),xcp(152:end),'k','LineWidth',1); hold on;
axis([-20 20 -20 20]);
set(gca,'FontName','Cambria','FontSize',11,'FontAngle','normal');
xlabel('\alpha','FontName','Cambria','FontSize',12,'FontAngle','italic');
ylabel('x_c_p /c','FontName','Cambria','FontSize',12,'FontAngle','italic');
%grid(alfa(152),'Color','k','LineStyle','--','LineWidth',0.5);         %plot asintoto verticale

%Calcolo del centro aerodinamico.

xac = (Cmc4(end) - Cmc4(end-1) + (Cl(end-1) - Cl(end))*0.25)/(Cl(end-1) - Cl(end))    %calcolo centro aerodinamico

%Cm rispetto al centro aerodinamico (costante al variare dell'angolo
%d'attacco).

Cmac = Cmc4 + Cl.*(xac - 0.25);

figure(9);                                                              %plot curva Cm_ac-alfa
plot(alfacopy,Cmac,'k--','LineWidth',1);
legend('Cm_c_/_4','Cm_a_c'); hold off;

